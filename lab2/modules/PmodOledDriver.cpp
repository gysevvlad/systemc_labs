#include "../modules/Cpu.h"
#include "../modules/PmodOledDriver.h"

void PmodOledDriver::setC()
{
    sc_uint<32> ADDR(0xFFFF0003);
    ADDR.range(15, 12) = m_device_number;
    m_cpu.write(ADDR, 0x00000000);
}

void PmodOledDriver::setD()
{
    sc_uint<32> ADDR(0xFFFF0005);
    ADDR.range(15, 12) = m_device_number;
    m_cpu.write(ADDR, 0x00000000);
}

void PmodOledDriver::send_command(const std::vector<unsigned char> & bytes)
{
    setC();
    send(bytes);
}

void PmodOledDriver::send_data(const std::vector<unsigned char> & bytes)
{
    setD();
    send(bytes);
}

void PmodOledDriver::init()
{
    static std::vector<unsigned char> bytes = {
        0xAE,       /* Set display off */
        0xD5, 0x80, /* Set Display Clock Divide Ratio/Oscillator Frequency */
        0xA8, 0x1F, /* Set Multiplex Ratio */
        0xD3, 0x00, /* Set Display Offset */
        0x40,       /* Set Display Start Line */
        0x8D, 0x10, /* Set Charge Pump */
        0xA1,       /* Set Segment Re-Map */
        0x81,       /* Set Contrast Control */
        0xA4,       /* Set Entire Display On/Off */
        0xA6,       /* Set Normal/Inverse Display */
        0xAF        /* Set Display On */
    };
    send_command(bytes);
}

void PmodOledDriver::sent_picture(bitmap_t & map)
{
    std::vector<unsigned char> data(32*16);
    for ( size_t i = 0; i < 32*16; i++ )
        data.push_back(map[i]);
    send_data(data);
}

